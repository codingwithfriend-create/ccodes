#include <iostream>

int main() {
    // Declare a variable to store the input
    int age;

    // Prompt the user for input
    std::cout << "Enter your age: ";

    // Take input from the keyboard and store it in 'age'
    std::cin >> age;

    // Output the result
    std::cout << "You are " << age << " years old!" << std::endl;

    return 0;
}